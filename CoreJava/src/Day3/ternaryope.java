package Day3;

public class ternaryope { // conditional operator

	public static void main(String[] args) {
		
		//  Syntax: variable=expression ?result1 : result2
		
		int a=12,b=23;
		int c=(a>b)? 100:200;
		System.out.println(c);//ans 200 why bcz if condition is true result 1 value will be print

	}

}
